package firstweek.day1;

public class PositiveNegativeNumberIdentification {

	public static void main(String[] args) {
		int number = -25;
		int number1 = 35;
		if (number<0) {
			System.out.println("The number " + number + " is Negative");
		}
		if (number1>0) {
		 System.out.println("The number " + number1 + " is Positive");
	}
		else
			System.out.println("The number is neither Negative nor Positive");
	}
}
